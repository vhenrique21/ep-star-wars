package exercicio_1;

public class Cla {

	//Essa classe representa um Clã, e tem seus atributos baseados na descrição de um 
	//clã do texto motivador para o exercicio.
	
	private String mestre;
	private String fundacao;
	
	public String getMestre() {
		return mestre;
	}
	public void setMestre(String mestre) {
		this.mestre = mestre;
	}
	public String getFundacao() {
		return fundacao;
	}
	public void setFundacao(String fundacao) {
		this.fundacao = fundacao;
	}
	
}
