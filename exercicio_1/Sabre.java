package exercicio_1;

public class Sabre {

	private Ser dono;
	
	private String tipoDeCristal;

	private String punho;

	public Ser getDono() {
		return dono;
	}
	public String getPunho() {
		return punho;
	}
	public void setPunho(String punho) {
		this.punho = punho;
	}
	public void setDono(Ser dono) {
		this.dono = dono;
	}
	public String getTipoDeCristal() {
		return tipoDeCristal;
	}
	public void setTipoDeCristal(String tipoDeCristal) {
		this.tipoDeCristal = tipoDeCristal;
	}

	
}
